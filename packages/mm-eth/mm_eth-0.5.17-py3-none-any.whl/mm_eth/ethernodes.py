    res = hr(url, proxy=proxy, user_agent=CHROME_USER_AGENT)
    if res.is_error():
        return res.to_err_result()
    try:
        return Ok(SearchResult(**res.json))
    except Exception as e:
        return res.to_err_result(f"exception: {e}")
